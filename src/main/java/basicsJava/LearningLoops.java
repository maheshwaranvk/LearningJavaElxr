package basicsJava;

import java.util.Iterator;

public class LearningLoops {
	
	public static void main(String[] args) {
		
		//System.out.println("Mahesh");
		int i=1;
		
		//while
		
		while (i <= 0) {
			
			System.out.println("Inside While Loop : Mahesh " + i);
			i++;
		}
		 
		do {
			System.out.println("Inside do..while Loop : Mahesh " + i);
			i++;
		} while (i <= 0);
		
		//for(int, condition, incre/dcrement)
		 
		for(int j=5; j>=0; j--) {
			 System.out.println("Inside for Loop : Mahesh " + j);
		 }
		
		//nested Loop --> Loop inside a Loop
		
		
		}
	}


