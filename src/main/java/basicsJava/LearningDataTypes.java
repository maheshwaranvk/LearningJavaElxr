package basicsJava;

public class LearningDataTypes {

	public static void main(String[] args) {
		
		//character --> hold a single alpha numeric character --> 0-9 & a-z
		
		char grade = 'A';
		char rank ='1';
		
		//String --> sequence of Character (Alpha Numeric)
		
		String name ="Elixr";
		String rollNumber = "EXR0056";
		
		// Integer --> number
		
		int salary = 1000;
		
		long salary2 = 100000000000000000L;
		
		// Float --> decimal value
		
		float pi = 3.14F;
		
		double pi2 = 3.142857142857143;

	}

}
